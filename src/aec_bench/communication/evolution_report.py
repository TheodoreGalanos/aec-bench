# ABOUTME: Self-contained HTML report renderer for evolution run results.
# ABOUTME: Produces a single HTML file with inline SVG chart and collapsible artifact diffs.

from __future__ import annotations

import html
from typing import TYPE_CHECKING

if TYPE_CHECKING:
    from aec_bench.evolution.report_data import CycleReport, EvolutionReportData


def render_evolution_report_html(data: EvolutionReportData) -> str:
    """Render a self-contained HTML report from evolution data."""
    cycles_html = "\n".join(_render_cycle_card(c) for c in data.cycles)
    chart_html = _render_svg_chart(data)

    return f"""\
<!DOCTYPE html>
<html lang="en">
<head>
<meta charset="utf-8">
<meta name="viewport" content="width=device-width, initial-scale=1">
<title>Evolution Report — {_esc(data.workspace_name)}</title>
{_CSS}
</head>
<body>

<header>
  <h1>Evolution Report</h1>
  <p class="subtitle">{_esc(data.workspace_name)}</p>
</header>

<main>
  {_render_overview(data)}
  {chart_html}
  <section class="cycles">
    {cycles_html if cycles_html else '<p class="empty">No evolution cycles recorded.</p>'}
  </section>
</main>

<footer>
  <p>Generated by aec-bench evolution engine</p>
</footer>

{_JS}
</body>
</html>"""


