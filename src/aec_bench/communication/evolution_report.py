def _render_overview(data: EvolutionReportData) -> str:
    status_class = "converged" if data.converged else "running"
    status_text = "Converged" if data.converged else "Not converged"
    return f"""\
<section class="overview">
  <div class="stat-grid">
    <div class="stat">
      <span class="stat-label">Cycles</span>
      <span class="stat-value">{data.total_cycles}</span>
    </div>
    <div class="stat">
      <span class="stat-label">Best Score</span>
      <span class="stat-value score">{data.best_score:.1%}</span>
    </div>
    <div class="stat">
      <span class="stat-label">Final Score</span>
      <span class="stat-value score">{data.final_score:.1%}</span>
    </div>
    <div class="stat">
      <span class="stat-label">Status</span>
      <span class="stat-value {status_class}">{status_text}</span>
    </div>
    <div class="stat">
      <span class="stat-label">Model</span>
      <span class="stat-value model">{_esc(data.model)}</span>
    </div>
  </div>
</section>"""


def _render_svg_chart(data: EvolutionReportData) -> str:
    if not data.cycles:
        return ""

    scores = [0.0] + [c.score for c in data.cycles]
    labels = ["evo-0"] + [c.version_tag for c in data.cycles]
    n = len(scores)
    if n < 2:
        return ""

    # Chart dimensions
    w, h = 700, 200
    pad_x, pad_y = 50, 30
    plot_w = w - 2 * pad_x
    plot_h = h - 2 * pad_y

    max_score = max(max(scores), 0.01)

    def _x(i: int) -> float:
        return pad_x + (i / (n - 1)) * plot_w

    def _y(s: float) -> float:
        return h - pad_y - (s / max_score) * plot_h

    # Build polyline points
    points = " ".join(f"{_x(i):.1f},{_y(s):.1f}" for i, s in enumerate(scores))

    # Build dots and labels
    dots = []
    for i, (s, label) in enumerate(zip(scores, labels, strict=True)):
        cx, cy = _x(i), _y(s)
        click = f' onclick="scrollToCycle({i})" style="cursor:pointer"' if i > 0 else ""
        label_y = cy - 12
        dots.append(
            f'<circle cx="{cx:.1f}" cy="{cy:.1f}" r="5" class="dot"{click}/>'
            f'<text x="{cx:.1f}" y="{label_y:.1f}" class="score-label">'
            f"{s:.1%}</text>"
            f'<text x="{cx:.1f}" y="{h - 5}" class="tag-label">'
            f"{_esc(label)}</text>"
        )

    return f"""\
<section class="chart">
  <h2>Score Trajectory</h2>
  <svg viewBox="0 0 {w} {h}" class="score-chart">
    <line x1="{pad_x}" y1="{h - pad_y}" x2="{w - pad_x}" y2="{h - pad_y}"
          stroke="#ccc" stroke-width="1"/>
    <line x1="{pad_x}" y1="{pad_y}" x2="{pad_x}" y2="{h - pad_y}"
          stroke="#ccc" stroke-width="1"/>
    <polyline points="{points}" class="score-line"/>
    {"".join(dots)}
  </svg>
</section>"""


def _render_cycle_card(cycle: CycleReport) -> str:
    skills_html = _render_skills_section(cycle)
    prompt_html = (
        _render_collapsible(
            "System Prompt Diff",
            _render_diff_block(cycle.prompt_diff),
            collapsed=True,
        )
        if cycle.prompt_diff
        else ""
    )

    reasoning_html = ""
    if cycle.evolver_reasoning:
        reasoning_html = f"""\
<div class="reasoning">
  <span class="section-label">Evolver Reasoning</span>
  <blockquote>{_esc(cycle.evolver_reasoning)}</blockquote>
</div>"""

    return f"""\
<article class="cycle-card" id="cycle-{cycle.cycle}">
  <div class="cycle-header">
    <h3>Cycle {cycle.cycle}</h3>
    <span class="version-tag">{_esc(cycle.version_tag)}</span>
    <span class="score-badge">{cycle.score:.1%}</span>
  </div>
  {prompt_html}
  {skills_html}
  {reasoning_html}
</article>"""


def _render_skills_section(cycle: CycleReport) -> str:
    if not cycle.skills_added and not cycle.skills_modified and not cycle.skills_removed:
        return ""

    pills: list[str] = []
    for name in cycle.skills_added:
        diff = cycle.skill_diffs.get(name, "")
        if diff:
            pills.append(
                _render_collapsible(
                    f'<span class="pill added">+ {_esc(name)}</span>',
                    _render_diff_block(diff),
                    collapsed=True,
                )
            )
        else:
            pills.append(f'<span class="pill added">+ {_esc(name)}</span>')

    for name in cycle.skills_modified:
        diff = cycle.skill_diffs.get(name, "")
        if diff:
            pills.append(
                _render_collapsible(
                    f'<span class="pill modified">~ {_esc(name)}</span>',
                    _render_diff_block(diff),
                    collapsed=True,
                )
            )
        else:
            pills.append(f'<span class="pill modified">~ {_esc(name)}</span>')

    for name in cycle.skills_removed:
        pills.append(f'<span class="pill removed">- {_esc(name)}</span>')

    return f"""\
<div class="skills-section">
  <span class="section-label">Skills</span>
  <div class="skills-list">
    {"".join(pills)}
  </div>
</div>"""


def _render_collapsible(summary: str, content: str, *, collapsed: bool = True) -> str:
    state = "" if collapsed else " open"
    return f"""\
<details{state}>
  <summary>{summary}</summary>
  <div class="collapsible-content">{content}</div>
</details>"""


def _render_diff_block(diff_text: str) -> str:
    if not diff_text:
        return '<pre class="diff empty">No changes</pre>'

    lines: list[str] = []
    for line in diff_text.splitlines():
        escaped = _esc(line)
        if line.startswith("+") and not line.startswith("+++"):
            lines.append(f'<span class="diff-add">{escaped}</span>')
        elif line.startswith("-") and not line.startswith("---"):
            lines.append(f'<span class="diff-del">{escaped}</span>')
        elif line.startswith("@@"):
            lines.append(f'<span class="diff-hunk">{escaped}</span>')
        else:
            lines.append(escaped)

    return f'<pre class="diff">{"<br>".join(lines)}</pre>'


# ---------------------------------------------------------------------------
# Utilities
# ---------------------------------------------------------------------------


def _esc(text: str) -> str:
    return html.escape(text)


