# ABOUTME: Generates self-contained Python CLI scripts from a TemplateConfig and engine source.
# ABOUTME: Generated scripts embed the engine, argparse args, format/help-params, and trace logging.

from aec_bench.templates.contracts import ParamType, TemplateConfig


def _param_name_to_cli_flag(name: str) -> str:
    """Convert snake_case param name to --kebab-case CLI flag."""
    return "--" + name.replace("_", "-")


def _build_param_info_dict(config: TemplateConfig) -> str:
    """Build the PARAM_INFO dict literal string for embedding in generated code."""
    lines = ["PARAM_INFO = {"]
    for name, spec in config.params.items():
        unit = spec.unit or ""
        type_str = spec.type.value
        min_val = repr(spec.min_value) if spec.min_value is not None else "None"
        max_val = repr(spec.max_value) if spec.max_value is not None else "None"
        values = repr(spec.values) if spec.values is not None else "None"
        lines.append(f'    "{name}": {{')
        lines.append(f'        "description": {repr(spec.description)},')
        lines.append(f'        "unit": {repr(unit)},')
        lines.append(f'        "type": {repr(type_str)},')
        lines.append(f'        "min": {min_val},')
        lines.append(f'        "max": {max_val},')
        lines.append(f'        "values": {values},')
        lines.append("    },")
    lines.append("}")
    return "\n".join(lines)


def _build_add_argument_calls(config: TemplateConfig) -> str:
    """Build parser.add_argument() call strings for each param in config."""
    lines = []
    for name, spec in config.params.items():
        flag = _param_name_to_cli_flag(name)
        unit = spec.unit or ""
        help_text = spec.description
        if unit:
            help_text = f"{help_text} ({unit})"

        if spec.type is ParamType.FLOAT:
            lines.append(f"    parser.add_argument({repr(flag)}, type=float, required=True, help={repr(help_text)})")
        elif spec.type is ParamType.INT:
            lines.append(f"    parser.add_argument({repr(flag)}, type=int, required=True, help={repr(help_text)})")
        elif spec.type is ParamType.ENUM:
            choices = repr(spec.values)
            lines.append(
                f"    parser.add_argument({repr(flag)}, choices={choices}, required=True, help={repr(help_text)})"
            )

    return "\n".join(lines)


def _build_inputs_dict(config: TemplateConfig) -> str:
    """Build the inputs dict construction that maps CLI args to compute() kwargs."""
    lines = ["    inputs = {"]
    for name in config.params:
        attr = name  # argparse stores kebab-case as underscore dest by default
        lines.append(f'        "{name}": getattr(args, "{attr}"),')
    lines.append("    }")
    return "\n".join(lines)


def generate_cli_wrapper(config: TemplateConfig, engine_source: str) -> str:
    """Generate a self-contained Python CLI script from a TemplateConfig and engine source.

    The returned string is complete, valid Python source. It embeds the engine source,
    derives argparse arguments from config.params, and logs every invocation to
    /logs/tool/invocations.jsonl.
    """
    param_info_block = _build_param_info_dict(config)
    add_argument_calls = _build_add_argument_calls(config)
    inputs_dict_block = _build_inputs_dict(config)
    template_name = config.meta.name

    generated = f'''\
# Auto-generated by aec-bench. Do not edit.
import argparse
import json
import sys
import time
from pathlib import Path

TRACE_LOG = Path("/logs/tool/invocations.jsonl")

# ---- Engine code (embedded) ----
{engine_source}
# ---- End engine code ----

{param_info_block}


def _log_trace(
    event: str,
    args: dict | None = None,
    output: dict | None = None,
    error: str | None = None,
    duration_ms: float | None = None,
) -> None:
    """Append a trace record to the invocations log.

    Silently skips logging if the log directory does not exist, so the script
    can be used outside the container environment during development.
    """
    try:
        TRACE_LOG.parent.mkdir(parents=True, exist_ok=True)
        record = {{
            "timestamp": time.strftime("%Y-%m-%dT%H:%M:%SZ", time.gmtime()),
            "template": {repr(template_name)},
            "event": event,
            "args": args,
            "output": output,
            "error": error,
            "duration_ms": duration_ms,
        }}
        with TRACE_LOG.open("a") as fh:
            fh.write(json.dumps(record) + "\\n")
    except Exception:
        pass


def _print_help_params() -> None:
    """Print a human-readable table of all parameter descriptions and ranges."""
    print(f"Parameters for {repr(template_name)}:")
    print()
    for name, info in PARAM_INFO.items():
        flag = "--" + name.replace("_", "-")
        unit = f" ({{info['unit']}})" if info["unit"] else ""
        type_str = info["type"]
        if info["values"] is not None:
            range_str = f"choices: {{info['values']}}"
        else:
            range_str = f"range: {{info['min']}} — {{info['max']}}"
        print(f"  {{flag}}{{unit}}")
        print(f"    type: {{type_str}}")
        print(f"    {{range_str}}")
        print(f"    {{info['description']}}")
        print()


def main() -> None:
    parser = argparse.ArgumentParser(
        description="Tool: {template_name}",
        formatter_class=argparse.RawDescriptionHelpFormatter,
    )
{add_argument_calls}
    parser.add_argument(
        "--format",
        choices=["json", "table"],
        default="json",
        help="Output format (default: json)",
    )
    parser.add_argument(
        "--help-params",
        action="store_true",
        default=False,
        help="Print parameter descriptions with types and ranges, then exit",
    )

    args = parser.parse_args()

    if args.help_params:
        _print_help_params()
        _log_trace("help")
        sys.exit(0)

{inputs_dict_block}

    start = time.monotonic()
    try:
        result = compute(**inputs)
    except Exception as exc:
        duration_ms = (time.monotonic() - start) * 1000
        _log_trace("error", args=inputs, error=str(exc), duration_ms=duration_ms)
        print(f"Error: {{exc}}", file=sys.stderr)
        sys.exit(1)

    duration_ms = (time.monotonic() - start) * 1000
    _log_trace("invocation", args=inputs, output=result, duration_ms=duration_ms)

    if args.format == "json":
        print(json.dumps(result, indent=2))
    else:
        for k, v in result.items():
            print(f"{{k}}: {{v}}")


if __name__ == "__main__":
    main()
'''
    return generated
