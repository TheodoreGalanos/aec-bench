from __future__ import annotations

from aec_bench.task_world_templates.contracts import (
    BranchDecisionSpec,
    CompositeTaskWorldStage,
    CompositeTaskWorldTemplate,
    DataGapSpec,
    DeliverableSpec,
    HandoffSpec,
    SourceArtifactSpec,
    VerifierGateSpec,
)


def list_templates() -> list[CompositeTaskWorldTemplate]:
    return list(_TEMPLATES)


def get_template(template_id: str) -> CompositeTaskWorldTemplate:
    for template in _TEMPLATES:
        if template.template_id == template_id:
            return template
    known = ", ".join(template.template_id for template in _TEMPLATES)
    msg = f"Unknown composite task-world template '{template_id}'. Known templates: {known}"
    raise KeyError(msg)


def _source(
    artifact_id: str,
    artifact_type: str,
    description: str,
    carries: list[str],
    modality: str = "text",
) -> SourceArtifactSpec:
    return SourceArtifactSpec(
        id=artifact_id,
        artifact_type=artifact_type,
        description=description,
        carries=carries,
        modality=modality,
    )


def _stage(
    stage_id: str,
    title: str,
    discipline: str,
    template_refs: list[str],
    consumes: list[str],
    produces: list[str],
    gates: list[str],
    branches: list[str] | None = None,
) -> CompositeTaskWorldStage:
    return CompositeTaskWorldStage(
        id=stage_id,
        title=title,
        discipline=discipline,
        template_refs=template_refs,
        consumes=consumes,
        produces=produces,
        branch_decisions=branches or [],
        verifier_gates=gates,
    )


def _handoff(
    handoff_id: str,
    description: str,
    unit: str,
    producer_stage: str,
    consumers: list[str],
    example_value: float | int | str,
    tolerance: float | None = None,
) -> HandoffSpec:
    return HandoffSpec(
        id=handoff_id,
        description=description,
        unit=unit,
        producer_stage=producer_stage,
        consumer_stages=consumers,
        example_value=example_value,
        tolerance=tolerance,
    )


def _branch(
    branch_id: str,
    description: str,
    allowed: list[str],
    selected: str,
    evidence_key: str,
) -> BranchDecisionSpec:
    return BranchDecisionSpec(
        id=branch_id,
        description=description,
        allowed=allowed,
        selected_example=selected,
        evidence_key=evidence_key,
    )


def _gate(
    gate_id: str,
    category: str,
    proposition: str,
    required_evidence: list[str],
    failure_effect: str,
) -> VerifierGateSpec:
    return VerifierGateSpec(
        id=gate_id,
        category=category,
        proposition=proposition,
        required_evidence=required_evidence,
        failure_effect=failure_effect,
    )


def _deliverable(deliverable_id: str, path: str, description: str) -> DeliverableSpec:
    return DeliverableSpec(id=deliverable_id, path=path, description=description)


def _gap(gap_id: str, description: str, severity: str = "medium") -> DataGapSpec:
    return DataGapSpec(id=gap_id, description=description, severity=severity)


